package com.example.clockwearable.bean;

public class Clock {
    private Integer clockid;
    private Integer years;
    private Integer months;
    private Integer days;
    private Integer hours;
    private Integer minutes;
    private Integer sends;
    private Integer happened;

    public Integer getClockid() {
        return clockid;
    }

    public void setClockid(Integer clockid) {
        this.clockid = clockid;
    }

    public Integer getYears() {
        return years;
    }

    public void setYears(Integer years) {
        this.years = years;
    }

    public Integer getMonths() {
        return months;
    }

    public void setMonths(Integer months) {
        this.months = months;
    }

    public Integer getDays() {
        return days;
    }

    public void setDays(Integer days) {
        this.days = days;
    }

    public Integer getHours() {
        return hours;
    }

    public void setHours(Integer hours) {
        this.hours = hours;
    }

    public Integer getMinutes() {
        return minutes;
    }

    public void setMinutes(Integer minutes) {
        this.minutes = minutes;
    }

    public Integer getSends() {
        return sends;
    }

    public void setSends(Integer sends) {
        this.sends = sends;
    }

    public Integer getHappened() {
        return happened;
    }

    public void setHappened(Integer happened) {
        this.happened = happened;
    }
}
